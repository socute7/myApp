package com.example.UAS;

public class LaporanPenjualan {
    int id;
    String username;
    String nama;
    int harga;
    String tanggal;

    public LaporanPenjualan(int id, String username, String nama, int harga, String tanggal) {
        this.id = id;
        this.username = username;
        this.nama = nama;
        this.harga = harga;
        this.tanggal = tanggal;
    }
}
