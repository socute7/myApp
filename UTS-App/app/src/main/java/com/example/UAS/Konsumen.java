package com.example.UAS;

public class Konsumen {
    int id;
    String nama;
    String username;
    int umur;
    String alamat;

    public Konsumen(int id, String nama, String username, int umur, String alamat) {
        this.id = id;
        this.nama = nama;
        this.username = username;
        this.umur = umur;
        this.alamat = alamat;
    }
}
